package array;

/**
 * 	数组
 * @author 徐葳
 *
 */
public class ArrDemo1 {
	public static void main(String[] args) {
		//	定义一个int类型数组，数组中存储3个元素
		int[] arr = new int[3];
		arr[0] = 10;
		
		/*
		 左边：int[] arr
		 int：表示数组中元素的类型
		 []：表示数组
		 arr：表示是数组的名称
		 
		  定义了一个数组类型的变量arr，数组中可以存储int类型的元素
		 简称为：定义一个int类型的数组，名称是arr
		 
		 
		 右边：new int[3]
		 new：用来创建实体的关键字
		 int：表示数组中元素的类型
		 []：表示数组
		 3：数组中可以存储的元素的个数
		 
		 新建一个int类型的数组，该数组中可以存储3个元素
		 */
		
	}

}
