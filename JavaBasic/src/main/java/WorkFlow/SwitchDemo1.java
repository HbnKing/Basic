package WorkFlow;

/**
 * switch案例
 * @author 徐葳
 *
 */
public class SwitchDemo1 {
	public static void main(String[] args) {
		int x = 3;
		switch(x) {
			case 1:
				System.out.println("A");
				break;
			case 2:
				System.out.println("B");
				break;
			case 3:
				System.out.println("C");
				break;
			case 4:
				System.out.println("D");
				break;
			default:
				System.out.println("default");
				break;
		}
	}

}
